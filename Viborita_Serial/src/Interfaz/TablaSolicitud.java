    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        //aqui se toman los datos de la fila seleccionada para crear la lista de los usuarios que podran jugar
        int filaSelect = jtTablaSolicitud.getSelectedRow();
        
        if (filaSelect == -1) {
            
           JOptionPane.showMessageDialog(this, "Seleccione Usuario Primero", "AVISO", 1);
            
        }
        else{
            System.out.println("filaSelect "+filaSelect);
            //se envia a la lista de aceptados 
            Usuario usuario = new Usuario(jtTablaSolicitud.getValueAt(filaSelect, 0).toString(), jtTablaSolicitud.getValueAt(filaSelect, 1).toString(), "0", "0", "1");
            viborita_serial.Viborita_Serial.ListaAceptados.add(usuario);
            JOptionPane.showMessageDialog(this, "Usuario Aceptado Exitosamente", "AVISO", 1);
            
            viborita_serial.Viborita_Serial.listaSolicitudes.remove(filaSelect);//elimina de la lista de solicitudes
            mostrar();
            
            
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

