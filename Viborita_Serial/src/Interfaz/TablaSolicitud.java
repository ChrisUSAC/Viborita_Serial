public void mostrar(){
    
    //creacion de la matriz para mostrar las filas
    String matriz[][]=new String[viborita_serial.Viborita_Serial.listaSolicitudes.size()][2];
    
    for (int i = 0; i < viborita_serial.Viborita_Serial.listaSolicitudes.size(); i++) {
        
        matriz[i][0]= viborita_serial.Viborita_Serial.listaSolicitudes.get(i).getUsuario();
        matriz[i][1]= viborita_serial.Viborita_Serial.listaSolicitudes.get(i).getContraseña();
        
    }
    
    
    jtTablaSolicitud.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Usuario", "Contraseña"
            }
        )); 
}

